var x=3;
switch (x) {
    case 1:
        console.log("X is 1");
        
        break;
    case 2:
        console.log("X is 2");
        
        break;
    case 3:
        console.log("X is 3");
        
        break;
    case 4:
        console.log("X is 4");
        
        break;

    default:
        console.log("X is default");
        break;
}